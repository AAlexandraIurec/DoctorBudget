package com.example.doctorBudget;

public class TopPFSIncome {
    private int psfID;
    private String pfsName;
    private Double sumAmountInc;

    public int getPsfID() {
        return psfID;
    }

    public void setPsfID(int psfID) {
        this.psfID = psfID;
    }

    public String getPfsName() {
        return pfsName;
    }

    public void setPfsName(String pfsName) {
        this.pfsName = pfsName;
    }

    public Double getSumAmountInc() {
        return sumAmountInc;
    }

    public void setSumAmountInc(Double sumAmountInc) {
        this.sumAmountInc = sumAmountInc;
    }
}
