package com.example.doctorBudget;

public class TopPSFExpense {
    private int psfID;
    private String pfsName;
    private Double sumAmountExp;

    public int getPsfID() {
        return psfID;
    }

    public void setPsfID(int psfID) {
        this.psfID = psfID;
    }

    public String getPfsName() {
        return pfsName;
    }

    public void setPfsName(String pfsName) {
        this.pfsName = pfsName;
    }

    public Double getSumAmountExp() {
        return sumAmountExp;
    }

    public void setSumAmountExp(Double sumAmountExp) {
        this.sumAmountExp = sumAmountExp;
    }
}
